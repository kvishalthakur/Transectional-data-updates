package com.tdu.TransactionDataUpdate.model;

public class Registers {

	private String size;
	private String mIUNumber;
	private String encoderId;
	private String oldRead;
	private String newRead;
	private String readType;
	private String dials;
	public String getSize() {
		return size;
	}
	public void setSize(String size) {
		this.size = size;
	}
	public String getmIUNumber() {
		return mIUNumber;
	}
	public void setmIUNumber(String mIUNumber) {
		this.mIUNumber = mIUNumber;
	}
	public String getEncoderId() {
		return encoderId;
	}
	public void setEncoderId(String encoderId) {
		this.encoderId = encoderId;
	}
	public String getOldRead() {
		return oldRead;
	}
	public void setOldRead(String oldRead) {
		this.oldRead = oldRead;
	}
	public String getNewRead() {
		return newRead;
	}
	public void setNewRead(String newRead) {
		this.newRead = newRead;
	}
	public String getReadType() {
		return readType;
	}
	public void setReadType(String readType) {
		this.readType = readType;
	}
	public String getDials() {
		return dials;
	}
	public void setDials(String dials) {
		this.dials = dials;
	}
	
	
}
